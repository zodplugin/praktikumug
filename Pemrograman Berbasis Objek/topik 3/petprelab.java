public class Pet
{
    private String nama,hewan;
    private int umur;
    
    public void setNama(String name){
        this.nama = name;
    }
    
    public void setHewan(String name){
        this.hewan = name;
    }
    
    public void setUmur(int umur){
        this.umur = umur;
    }
    
    public String getNama(){
        return nama;
    }
    public String getHewan(){
        return hewan;
    }
    public int getUmur(){
        return umur;
    }
    
}
